# declare a string
mystr="Hello Everyone !"

# length of string
print(len(mystr))

# capitalize
print(mystr.capitalize())

# swap case
print(mystr.swapcase())

# replace word
print(mystr.replace('Hello','hi'))

# start with
print(mystr.startswith("Hello"))

# end with
print(mystr.endswith("anything"))

# split
print(mystr.split())

# find word array[]
print(mystr.find("o"))

# index
print(mystr.index("o"))